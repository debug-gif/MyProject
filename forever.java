
public class forever {
	public static void main(String[] args) {
       int x = 100;
	   switch(x) {
	   case 1:
	   System.out.println("x=1");
	   case 5:
		   System.out.println("x=5");
	   case 10:
		   System.out.println("x=10");
	   case 100:
		   x++;
		   System.out.println("x=101");	   
	   }
	}

}
